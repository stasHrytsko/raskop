// ───────────────────────────────────────────────
//  Раскоп — игровые константы (весь баланс здесь)
//  ПАРАМЕТРЫ ДВИЖКА — выверены Монте-Карло (бот ≈ сильный игрок, 6000 симуляций)
// ───────────────────────────────────────────────
//
//  FIELD = 6                 6×6
//  TRAPS = 10                было 4 — при 4 игра не работает (нет спреда стратегий)
//  COINS = 4
//  FIRST_TAP_BLANK = true    первый тап = 0 ловушек среди соседей (честный старт)
//  GUARANTEE_MAX_CLUE = 3    реген, если у безопасной клетки >3 ловушек рядом
//  MULT = {2:8, 3:14, 4:20}  ×2 @8, ×3 @14, ×4 @20  (было 6/9 — слишком низко)
//
//  ── ЗАХОД: ожидаемый выхлоп ──────────────────────────────────────────────
//  strategy    survive   E[pts]   E[pts|survive]   coins|surv
//   cautious    0.97      8.3        8.6             2.7   не угадывает
//   medium      0.95      8.5        9.0             2.8   риск ≤ 1/4
//   greedy      0.60      8.8       14.8             4.0   риск ≤ 1/2
//
//  Ключевое: безусловный EV почти ровный (8.3/8.5/8.8) — жадность покупает
//  дисперсию, не очки. Свободная зона при 10 ловушках ≈ 14 клеток (медиана):
//  ×2–×3 даром, ×4 и последние монеты — за риском.
// ───────────────────────────────────────────────
const SIZE = 6;            // поле SIZE × SIZE  (FIELD)
const COINS = 4;           // монет на заход (каждая = +1)
const TRAPS = 10;          // ловушек на поле (фикс. для всех уровней — выверено MC)

const LEVELS_COUNT = 9;    // уровней в одном забеге
const QUOTA_START = 6;     // квота первого уровня (в монетах)
const QUOTA_STEP = 2;      // +монет к квоте за каждый следующий уровень
const DIVES = 3;           // заходов на уровень

const FIRST_TAP_BLANK = true;  // первый тап захода — 0 ловушек среди соседей
const MAX_CLUE = 3;        // GUARANTEE_MAX_CLUE: цифры на поле только 1–3

// Пороги множителя жадности (число безопасных вскрытий за заход)
const TH2 = 8;             // ×2 @ 8 безопасных вскрытий
const TH3 = 14;            // ×3 @ 14
const TH4 = 20;            // ×4 @ 20 (за пределами медианной свободной зоны — за риском)

const REC_KEY = 'raskop_best';   // ключ рекорда в localStorage

// Квота, число ловушек и тир сложности уровня n (0-индекс).
// Ловушки фиксированы (TRAPS=10): сложность растёт только квотой.
const quotaFor = n => QUOTA_START + QUOTA_STEP * n;
const trapsFor = n => TRAPS;
const tierFor  = n => n < 3 ? 'easy' : n < 6 ? 'med' : 'hard';

